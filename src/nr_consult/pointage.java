 public void Deplace() {
        try {

            int row = table_pointage.getSelectedRow();
            this.test = (table_pointage.getModel().getValueAt(row, 0).toString());
            String requet = " select * from  pointage where id_pointage = '" + test + "' ";
            ps = conn.prepareStatement(requet);
            rs = ps.executeQuery();

            if (rs.next()) {
                String t1 = rs.getString("id_pointage");
                txt_id.setText(t1);
               String t2 = rs.getString("nom_employe");
                txt_nom.setText(t2);
                String t3 = rs.getString("prenom_employe");
                txt_prenom.setText(t3);
                String t4 = rs.getString("date_entre");
                txt_date.setText(t4);
                String t5 = rs.getString("heure_entre");
                txt_he.setText(t5);
                String t6 = rs.getString("heure_sortie");
                txt_hs.setText(t6);
                
                String t7 = rs.getString("etat");
                txt_e.setText(t7);
                
//                byte[] imagedata =rs.getBytes("image");
//                Format = new ImageIcon(imagedata);
//                image.setIcon(Format);
                
            }
ps.close();
                rs.close();
        } catch (Exception e) {
            System.out.println(e);
            
        }finally {

            try {
                ps.close();
                rs.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "erreur BD");
            }
        }
    }

