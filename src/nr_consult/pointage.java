    private void btnsupprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsupprActionPerformed
try {
            if (JOptionPane.showConfirmDialog(null, "attention vous devez suprimer un pointage,est ce que vous êtes sur?",
                    "Supprimer le pointage", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
                if (txt_id.getText().length() != 0) {

                    String requete = "delete from pointage where id_pointage = ?";
                    ps = conn.prepareStatement(requete);
                    ps.setString(1, txt_id.getText());
                    ps.execute();
                    System.out.println("deleted");
                    //JOptionPane.showMessageDialog(null,"deleted");
                    
                } else {
                    JOptionPane.showMessageDialog(null, "veuillez choisir  un pointage !");
                }
            }
              ps.close();
                rs.close();
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "erreur de supprimer \n" + e.getMessage());
        }finally {

            try {
                ps.close();
                rs.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "erreur BD");
            }
        } 
       
Clear();
        Affichage();
         // TODO add your handling code here:
    }//GEN-LAST:event_btnsupprActionPerformed

